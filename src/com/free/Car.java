package com.free;

public class Car {
	public String name;
	public double price;
	public int code;
	public int num;
	public double ton;
	public Car(String name, double price, int code, int num, double ton) {
		super();
		this.name = name;
		this.price = price;
		this.code = code;
		this.num = num;
		this.ton = ton;
	}
}
