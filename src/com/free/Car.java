package com.free;

public class Car {
	int item;
	String name;
	double price;
	int personNum;
	double tonNum;
	public Car(int item, String name, double price, int personNum, double tonNum) {
		super();
		this.item = item;
		this.name = name;
		this.price = price;
		this.personNum = personNum;
		this.tonNum = tonNum;
	}
	

}
